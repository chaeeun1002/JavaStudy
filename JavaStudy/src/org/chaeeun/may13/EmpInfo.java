package org.chaeeun.may13;

public class EmpInfo {
	private String emp_id;
	private String emp_name;
	private String emp_dpt;
	private String emp_gen;
	private String emp_addr;
	private String emp_date;
	public String getEmp_id() {
		return emp_id;
	}
	public void setEmp_id(String emp_id) {
		this.emp_id = emp_id;
	}
	public String getEmp_name() {
		return emp_name;
	}
	public void setEmp_name(String emp_name) {
		this.emp_name = emp_name;
	}
	public String getEmp_dpt() {
		return emp_dpt;
	}
	public void setEmp_dpt(String emp_dpt) {
		this.emp_dpt = emp_dpt;
	}
	public String getEmp_gen() {
		return emp_gen;
	}
	public void setEmp_gen(String emp_gen) {
		this.emp_gen = emp_gen;
	}
	public String getEmp_addr() {
		return emp_addr;
	}
	public void setEmp_addr(String emp_addr) {
		this.emp_addr = emp_addr;
	}
	public String getEmp_date() {
		return emp_date;
	}
	public void setEmp_date(String emp_date) {
		this.emp_date = emp_date;
	}
	
}
